package corejava;
//Parent
public class Oops_Inheritance1 {
	
	
	public static void add() {
		// DataType Objet = Value;
		int a = 90;
		int b = 80;
		System.out.println("Add Results is: " + (a + b));
	}

	public static void sub() {
		// DataType Objet = Value;
		int a = 500;
		int b = 50;
		System.out.println("Sub Results is---> " + (a - b));
	}
}
